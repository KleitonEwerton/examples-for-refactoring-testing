package org.testes;

public class Animal {
    String nome;
    double idade;
    String tipo;

    public String getNome() {
        return nome;
    }

    public double getIdade() {
        return idade;
    }

    public String getTipo() {
        return tipo;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public void setIdade(double idade) {
        this.idade = idade;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
}
